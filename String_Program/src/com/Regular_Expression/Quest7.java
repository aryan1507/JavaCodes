package com.Regular_Expression;

public class Quest7 {

	public static void main(String[] args) {
		int i=10;
		//Integer a=new Integer(i);
		Integer a=Integer.valueOf(i);
		System.out.println(i);//Primitive Value
		System.out.println(a);//Non-primitive value
        Integer b=new Integer(10);
        int c=Integer.valueOf(b);
        System.out.println(b);//Non-primitive value
        System.out.println(c);//Primitive value
        
	}

}
